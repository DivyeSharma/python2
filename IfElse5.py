# A COMPANY DEDICATED TO GIVE BONUS OF 1000Rs EMPLOYEE IF HIS/HER SERVICE IS MORE THAN 5 YEARS 
# ASKS USER THEIR SALARY AND YEARS OF SERVICES AND PRINT
# THE NET BONUS AMOUNT
 
Salary = int(input("Enter Employee Salary : "))
Year = int(input("Enter Your year of services : "))
if Year>5:
    print("Your Net Bonus Amount is ",Salary+1000)
else :
    print("Your Net Bonus Amount is ",Salary+0)

                 